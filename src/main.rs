            b[i].z = 0.5 * (1.0 + delta.cos());
            b[i].w = 1.0;

            // let i = 1;
            // b[i].x = if b[i].x >= 1.0 { 0.0 } else { b[i].x + 0.01 };
            // b[i].y = 1.0;
